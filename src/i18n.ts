export type UIKey = keyof typeof ui.en;

export function t(lang: Lang, key: UIKey): string {
  return (ui[lang] as Record<string, string>)[key] ?? (ui.en as Record<string, string>)[key] ?? key;
}

export function pathForLang(lang: Lang, currentPath: string, base: string): string {
  // currentPath is the path after the base, e.g. "/map/europe/" or "/fr/map/europe/"
  const withoutFr = currentPath.replace(/^\/fr/, '') || '/';
  if (lang === 'en') return `${base}${withoutFr.replace(/^\//, '')}`;
  return `${base}fr${withoutFr}`;
}

export function frequencyLabel(lang: Lang, freq: number): string {
  if (freq >= 8) return t(lang, 'freq.veryCommon');
  if (freq >= 5) return t(lang, 'freq.common');
  if (freq >= 3) return t(lang, 'freq.regular');
  if (freq >= 1) return t(lang, 'freq.rare');
  return t(lang, 'freq.private');
}

export function categoryLabel(lang: Lang, cat: string): string {
  const key = `cat.${cat}` as UIKey;
  return t(lang, key);
}

export function mapLabel(lang: Lang, slug: string, fallback?: string, translated?: string): string {
  if (lang === 'fr') {
    const key = `map.${slug}`;
    const fr = (ui.fr as Record<string, string>)[key];
    if (fr) return fr;
  }
  return translated || fallback || slug;
}
